package com.ayane.helpers;

import javax.swing.JTextField;

public class UITools {
    public void clearTextFields(JTextField a, JTextField b) {
        a.setText("");
        b.setText("");
        return;
    }

    public void clearTextFields(JTextField a, JTextField b, JTextField c) {
        a.setText("");
        b.setText("");
        c.setText("");
        return;
    }
}
